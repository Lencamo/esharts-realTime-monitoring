<template>
  <div class="hotPart-container">
    <div class="chart-container" ref="hot_ref"></div>
    <!-- 后面两个字体图标必须放到后面🤔 -->
    <span class="iconfont left-arrow" @click="toLeft">&#xe61e;</span>
    <span class="iconfont right-arrow" @click="toRight">&#xe61f;</span>
    <span class="pieData-name">{{ pieName }}</span>
  </div>
</template>
<script>
export default {
  computed: {
    pieName() {
      if (!this.pieData) {
        return ''
      } else {
        // bug 🚩 解决：前面我们把 pieData: []这样会导致报错，如下：
        //  Cannot read properties of undefined (reading 'getAttribute')
        //  Cannot read properties of undefined (reading 'name')
        return this.pieData[this.currentIndex].name
      }
    }
  },
  mounted() {
    this.initChart()
    this.getPieData()

    window.addEventListener('resize', this.screenAdapter)
    // 屏幕适配
    this.screenAdapter()
  },
  destroyed() {
    window.removeEventListener('resize', this.screenAdapter)
  },
  data() {
    return {
      chartInstance: null, // 空对象
      pieData: null,
      currentIndex: 0 // 要显示的哪部分的饼图数据
    }
  },
  methods: {
    // 初始化ECharts对象
    initChart() {
      this.chartInstance = this.$echarts.init(this.$refs.hot_ref, 'chalk')

      const initOption = {
        // 标题配置
        title: {
          text: '▎热销商品销售金额占比统计',
          left: 20,
          top: 20,
          textStyle: {
            fontSize: 38
          }
        },

        series: [
          {
            type: 'pie'
          }
        ]
      }
      // 生成图表
      this.chartInstance.setOption(initOption)
    },

    // 获取图表数据
    async getPieData() {
      const { data: res } = await this.$http.get('/api/hotproduct')
      // console.log(res)

      this.pieData = res
      this.updateBarGenerate()

      // 数据处理✨
    },

    // 更新图表（option配置）
    updateBarGenerate() {
      // 数据处理✨
      // 1、饼图第一层数据
      const firstSeriesData = this.pieData[this.currentIndex].children.map(
        (item) => {
          return {
            name: item.name,
            value: item.value
          }
        }
      )
      // 2、图例数据
      const legendData = this.pieData[this.currentIndex].children.map(
        (item) => {
          return item.name
        }
      )

      // option配置
      const dataOption = {
        legend: {
          data: legendData
        },
        series: [
          {
            data: firstSeriesData
          }
        ]
      }
      // 生成图表
      this.chartInstance.setOption(dataOption)
    },

    // 监听window窗口大小变化
    screenAdapter() {
      // const standFontSize = (this.$refs.hot_ref.offsetWidth / 100) * 3.6

      const adapterOption = {}
      // 生成图表
      this.chartInstance.setOption(adapterOption)

      // 手动的调用图表对象的resize 才能产生效果
      this.chartInstance.resize()
    },

    toLeft() {
      this.currentIndex--
      if (this.currentIndex < 0) {
        this.currentIndex = this.pieData.length - 1
      }
      this.updateBarGenerate()
    },

    toRight() {
      this.currentIndex++
      if (this.currentIndex > this.pieData.length - 1) {
        this.currentIndex = 0
      }
      this.updateBarGenerate()
    }
  }
}
</script>
